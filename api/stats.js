export default async function handler(req, res) {
    // CORS headers
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    
    if (req.method === 'OPTIONS') {
        return res.status(200).end();
    }

    if (req.method !== 'POST') {
        return res.status(405).json({ error: 'Method not allowed' });
    }

    try {
        const { completedActivities, energyLevel, completedTasks } = req.body;
        
        console.log('üìä POST /api/stats - –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫:', req.body);
        
        // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫
        const statsUpdate = {
            timestamp: new Date().toISOString(),
            completedActivities: completedActivities || [],
            energyLevel: energyLevel || 8,
            completedTasks: completedTasks || 0,
            xpGained: (completedTasks || 0) * 10
        };

        console.log('‚úÖ –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –æ–±–Ω–æ–≤–ª–µ–Ω—ã:', statsUpdate);
        
        // TODO: –°–æ—Ö—Ä–∞–Ω–∏—Ç—å –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö
        
        res.json({ 
            success: true, 
            message: '–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –æ–±–Ω–æ–≤–ª–µ–Ω—ã',
            stats: statsUpdate
        });
    } catch (error) {
        console.error('‚ùå Stats error:', error);
        res.status(500).json({ error: 'Internal server error' });
    }
}


